<script setup>
import { ref, onMounted, onUnmounted } from 'vue'

// 轮播图相关状态
const currentSlide = ref(0)
const totalSlides = ref(2)
const isAutoPlay = ref(true)
const autoPlayInterval = ref(null)

// 轮播图元素引用
const swiperContainer = ref(null)
const swiperWrapper = ref(null)
const swiperSlides = ref([])
const nextButton = ref(null)
const prevButton = ref(null)

// 初始化轮播图
const initSwiper = () => {
  // 获取轮播图元素
  swiperContainer.value = document.querySelector('.swiper-container')
  swiperWrapper.value = document.querySelector('.swiper-wrapper')
  swiperSlides.value = document.querySelectorAll('.swiper-slide')
  nextButton.value = document.querySelector('.swiper-button-next')
  prevButton.value = document.querySelector('.swiper-button-prev')
  
  if (swiperContainer.value && swiperWrapper.value && swiperSlides.value.length > 0) {
    totalSlides.value = swiperSlides.value.length
    updateSwiperDimensions()
    setupEventListeners()
    startAutoPlay()
    
    console.log('轮播图初始化完成，幻灯片数量:', totalSlides.value)
  }
}

// 设置事件监听器
const setupEventListeners = () => {
  // 左右按钮点击事件
  if (nextButton.value) {
    nextButton.value.addEventListener('click', nextSlide)
  }
  if (prevButton.value) {
    prevButton.value.addEventListener('click', prevSlide)
  }
  
  // 鼠标悬停事件
  if (swiperContainer.value) {
    swiperContainer.value.addEventListener('mouseenter', handleMouseEnter)
    swiperContainer.value.addEventListener('mouseleave', handleMouseLeave)
  }
  
  // 监听窗口大小变化，重新计算高度
  window.addEventListener('resize', () => {
    setTimeout(() => {
      updateSwiperHeight()
    }, 100)
  })
  
  // 监听图片加载完成，重新计算高度
  const images = swiperContainer.value?.querySelectorAll('img')
  if (images) {
    images.forEach(img => {
      img.addEventListener('load', () => {
        setTimeout(() => {
          updateSwiperHeight()
        }, 100)
      })
    })
  }
}

// 更新轮播图尺寸（响应式）
const updateSwiperDimensions = () => {
  if (swiperContainer.value && swiperWrapper.value) {
    const containerWidth = swiperContainer.value.offsetWidth
    
    // 设置每个幻灯片的宽度为容器宽度的100%
    swiperSlides.value.forEach(slide => {
      if (slide) {
        slide.style.width = containerWidth + 'px'
      }
    })
    
    // 更新swiper-wrapper的宽度
    swiperWrapper.value.style.width = (containerWidth * totalSlides.value) + 'px'
    
    // 更新高度自适应
    updateSwiperHeight()
    
    // 更新当前位置
    updateSlidePosition()
  }
}

// 更新轮播图高度（自适应）
const updateSwiperHeight = () => {
  if (swiperContainer.value && swiperSlides.value.length > 0) {
    let maxHeight = 0
    
    // 计算所有幻灯片的最大高度
    swiperSlides.value.forEach(slide => {
      if (slide) {
        // 临时重置高度以获取真实高度
        const originalHeight = slide.style.height
        slide.style.height = 'auto'
        const slideHeight = slide.offsetHeight
        slide.style.height = originalHeight // 恢复原高度
        
        if (slideHeight > maxHeight) {
          maxHeight = slideHeight
        }
      }
    })
    
    if (maxHeight > 0) {
      // 设置容器和包装器的高度
      swiperContainer.value.style.height = maxHeight + 'px'
      swiperWrapper.value.style.height = maxHeight + 'px'
      
      // 设置所有幻灯片的高度一致
      swiperSlides.value.forEach(slide => {
        if (slide) {
          slide.style.height = maxHeight + 'px'
        }
      })
      
      console.log('轮播图高度已更新:', maxHeight + 'px')
    }
  }
}

// 更新幻灯片位置
const updateSlidePosition = () => {
  if (swiperWrapper.value && swiperContainer.value) {
    const containerWidth = swiperContainer.value.offsetWidth
    const translateX = -currentSlide.value * containerWidth
    swiperWrapper.value.style.transform = `translate3d(${translateX}px, 0px, 0px)`
    swiperWrapper.value.style.transitionDuration = '300ms'
  }
}

// 下一张幻灯片
const nextSlide = () => {
  currentSlide.value = (currentSlide.value + 1) % totalSlides.value
  updateSlidePosition()
  // 延迟更新高度，确保DOM已更新
  setTimeout(() => {
    updateSwiperHeight()
  }, 50)
  console.log('切换到下一张:', currentSlide.value)
}

// 上一张幻灯片
const prevSlide = () => {
  currentSlide.value = currentSlide.value === 0 ? totalSlides.value - 1 : currentSlide.value - 1
  updateSlidePosition()
  // 延迟更新高度，确保DOM已更新
  setTimeout(() => {
    updateSwiperHeight()
  }, 50)
  console.log('切换到上一张:', currentSlide.value)
}

// 跳转到指定幻灯片
const goToSlide = (index) => {
  if (index >= 0 && index < totalSlides.value) {
    currentSlide.value = index
    updateSlidePosition()
    // 延迟更新高度，确保DOM已更新
    setTimeout(() => {
      updateSwiperHeight()
    }, 50)
    console.log('跳转到幻灯片:', currentSlide.value)
  }
}

// 开始自动播放
const startAutoPlay = () => {
  if (isAutoPlay.value && totalSlides.value > 1) {
    autoPlayInterval.value = setInterval(() => {
      nextSlide()
    }, 5000) // 5秒切换一次
  }
}

// 停止自动播放
const stopAutoPlay = () => {
  if (autoPlayInterval.value) {
    clearInterval(autoPlayInterval.value)
    autoPlayInterval.value = null
  }
}

// 切换自动播放
const toggleAutoPlay = () => {
  isAutoPlay.value = !isAutoPlay.value
  if (isAutoPlay.value) {
    startAutoPlay()
  } else {
    stopAutoPlay()
  }
}

// 窗口大小变化处理
const handleResize = () => {
  clearTimeout(window.resizeTimeout)
  window.resizeTimeout = setTimeout(() => {
    updateSwiperDimensions()
  }, 100)
}

// 鼠标悬停暂停自动播放
const handleMouseEnter = () => {
  stopAutoPlay()
}

// 鼠标离开恢复自动播放
const handleMouseLeave = () => {
  if (isAutoPlay.value) {
    startAutoPlay()
  }
}

// 组件挂载时初始化
onMounted(() => {
  setTimeout(() => {
    initSwiper()
    
    // 添加窗口大小变化监听
    window.addEventListener('resize', handleResize)
  }, 200)
})

// 组件卸载时清理
onUnmounted(() => {
  stopAutoPlay()
  window.removeEventListener('resize', handleResize)
  
  // 清理按钮事件监听器
  if (nextButton.value) {
    nextButton.value.removeEventListener('click', nextSlide)
  }
  if (prevButton.value) {
    prevButton.value.removeEventListener('click', prevSlide)
  }
  
  // 清理鼠标事件监听器
  if (swiperContainer.value) {
    swiperContainer.value.removeEventListener('mouseenter', handleMouseEnter)
    swiperContainer.value.removeEventListener('mouseleave', handleMouseLeave)
  }
  
  if (window.resizeTimeout) {
    clearTimeout(window.resizeTimeout)
  }
})

// 暴露方法供模板使用
defineExpose({
  nextSlide,
  prevSlide,
  goToSlide,
  toggleAutoPlay,
  currentSlide,
  totalSlides,
  isAutoPlay
})
</script>

