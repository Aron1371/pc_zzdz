import { useRoute } from "vue-router";

const route = useRoute();

// 当前激活的tab
const activeTab = ref("国资要闻");

// tab列表
const tabs = [
  { key: "国资要闻", label: "◈ 国资要闻" },
  { key: "集团新闻", label: "◈ 集团新闻" },
  { key: "新闻聚焦", label: "◈ 新闻聚焦" },
];

// 切换tab
const switchTab = (tabKey) => {
  activeTab.value = tabKey;
  console.log("切换到tab:", tabKey);
};

// 检查tab是否激活
const isTabActive = (tabKey) => {
  return activeTab.value === tabKey;
};

// 初始化
onMounted(() => {
  // 检查URL参数中的tab参数
  const tabParam = route.query.tab;
  if (tabParam && tabs.some((tab) => tab.key === tabParam)) {
    activeTab.value = tabParam;
  }

  console.log("新闻页面tab切换功能已初始化，当前tab:", activeTab.value);
});
</script>

